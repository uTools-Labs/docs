<h2 id="utools-能做什么"><a class="header-anchor" href="#utools-能做什么">#</a> uTools 能做什么？</h2>
<p>最简单的，uTools 可以作为一个程序快速启动器，支持英文、英文驼峰、中文拼音、拼音首字母来打开你的本地程序。除程序外， win10 和 macOS 用户还可以快速搜索并打开「控制面板」内的细项。总之，你只要还记得一个大概的名字，直接输入基本都能找到。</p>
<blockquote>
<p>针对中文特别优化，例如可以通过搜索 <code>记事本</code> 或 <code>jsb</code> 打开记事本，而不是搜索 <code>notepad</code></p>
</blockquote>
