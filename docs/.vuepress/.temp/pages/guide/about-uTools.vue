<h2 id="utools-和-「猿料」-是什么关系"><a class="header-anchor" href="#utools-和-「猿料」-是什么关系">#</a> uTools 和 「猿料」 是什么关系？</h2>
<p>uTools 一开始叫做猿料，意思是：程序员需要的材料/工具。</p>
<p>当我们开始为猿料寻找域名时，无意间发现了 u.tools ，感觉和我们想做的产品内容、域名、寓意都完美融合，遂改名叫 uTools 。对猿料这个名字依然喜爱，现在作为我们公众号和社区的名称。</p>
