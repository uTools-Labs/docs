<h2 id="数据同步"><a class="header-anchor" href="#数据同步">#</a> 数据同步</h2>
<p>uTools 是一个桌面应用程序，各插件所产生配置、内容都将保存在你本地数据库中。</p>
<p>但是你可能需要在公司、家里不同的设备上面使用 uTools ，或者为了预防重装系统等原因造成数据丢失。我们特别准备了云端实时同步备份服务，你可以在成为 uTools 会员 之后进入「设置」自助开启。</p>
