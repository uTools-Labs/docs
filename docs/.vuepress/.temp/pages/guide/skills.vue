<h2 id="esc-键-快速回到主输入框"><a class="header-anchor" href="#esc-键-快速回到主输入框">#</a> ESC 键 快速回到主输入框</h2>
<p>在任何进入插件后，可以通过 <code>esc</code> 快速回到初始状态。</p>
<h2 id="窗口分离"><a class="header-anchor" href="#窗口分离">#</a> 窗口分离</h2>
<p>uTools 插件都自带图形界面，或许有些时候你希望长时间使用某个插件，或是希望同时开启多个相同的插件，这个时候，你只需按 <code>ctrl + d</code>（ mac 下 <code>command + d</code> ），即可以将插件脱离 uTools 主输入框，把插件当成一个独立的应用程序来使用。</p>
<h2 id="自动识别"><a class="header-anchor" href="#自动识别">#</a> 自动识别</h2>
<p>安装对应的插件后，uTools 可以自动识别文本格式，并列出能够处理此文本的插件供你选择。</p>
<p>例如：</p>
<ul>
<li>通过超级面板划词翻译：<code>复制一段英文</code>-&gt;<code>通过长按右键或者中键唤出超级面板</code>-&gt;<code>选择「翻译」插件</code>-&gt;<code>松开鼠标按键得到结果</code>。</li>
<li>通过输入框编辑  JSON ：<code>复制一段 json 字符串</code>-&gt;<code>呼出 uTools (会自动粘贴)</code>-&gt;<code>选择「 json 」插件</code>-&gt;<code>得到结果</code>。</li>
</ul>
<h2 id="全局快捷键"><a class="header-anchor" href="#全局快捷键">#</a> 全局快捷键</h2>
<p>uTools 已经可以快速的辅助你解决一些小场景的问题，但是在使用很频繁的一些场景，合理的使用全局快捷键可以一键直达，快上加快。</p>
<p>首先，你需要在「 setting 」中配置全局快捷键，事例如下图：
<img src="https://res.u-tools.cn/website/shortcuts.png" alt="setting.png" style="zoom:50%;" /></p>
<ul>
<li>设置 <code>F1 </code> 为「百度」功能，按 <code>F1</code> 将打开插件，如果启用『自动复制』，将自动打开浏览器搜索当前选中的内容。</li>
<li>设置 <code>F2</code> 为「翻译」功能，当复制一段文本后，在5秒内按 <code>F2</code> 直接翻译复制的文本。</li>
<li>设置 <code>F3</code> 为「截图并自动识别」功能，将调用系统截图，完成截图后，进入插件自动识别图片中的文字。</li>
<li>设置 <code>F4</code> 为「截图」功能，将调用系统截图。</li>
</ul>
